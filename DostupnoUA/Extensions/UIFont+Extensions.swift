import UIKit

extension UIFont {
    
    static var p1LeftBold: UIFont {
        return UIFont.boldSystemFont(ofSize: 15)
    }
    
    static var p1LeftRegular: UIFont {
        return UIFont.systemFont(ofSize: 15)
    }
    
    static var h2LeftBold: UIFont {
        return UIFont.boldSystemFont(ofSize: 18)
    }
    
    static var p2LeftRegular: UIFont {
        return UIFont.systemFont(ofSize: 13)
    }
    
    static var p2LeftBold: UIFont {
        return UIFont.boldSystemFont(ofSize: 13)
    }
    
    static var p3LeftBold: UIFont {
        return UIFont.boldSystemFont(ofSize: 11)
    }
    
    static var p3LeftRegular: UIFont {
        return UIFont.systemFont(ofSize: 11)
    }
    
    static var h1CenteredBold: UIFont {
        return UIFont.boldSystemFont(ofSize: 21)
    }
}
